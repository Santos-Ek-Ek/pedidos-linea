// Carrito como array de objetos
let cart = [];

// Función mejorada para guardar el carrito
function saveCartToStorage() {
    if (cart.length > 0) {
        localStorage.setItem('shoppingCart', JSON.stringify(cart));
    } else {
        localStorage.removeItem('shoppingCart'); // Eliminar completamente si está vacío
    }
    // Disparar evento de actualización
    const event = new Event('cartUpdated');
    window.dispatchEvent(event);
}

// Función mejorada para sincronizar el carrito
function syncCartFromStorage() {
    const cartData = localStorage.getItem('shoppingCart');
    if (cartData) {
        try {
            cart = JSON.parse(cartData);
        } catch (e) {
            console.error('Error parsing cart:', e);
            cart = [];
        }
    } else {
        cart = [];
    }
    updateCartView();
}

// Función para limpiar completamente el carrito
function clearCart() {
    cart = [];
    localStorage.removeItem('shoppingCart');
    updateCartView();
    // Disparar evento de limpieza
    const event = new Event('cartCleared');
    window.dispatchEvent(event);
}

// Inicialización mejorada
document.addEventListener('DOMContentLoaded', function() {
    // Verificar si debemos limpiar el carrito (viene de checkout)
    const urlParams = new URLSearchParams(window.location.search);
    if (urlParams.has('clearCart')) {
        clearCart();
        // Limpiar el parámetro de la URL sin recargar
        window.history.replaceState({}, document.title, window.location.pathname);
    }
    
    // Cargar carrito inicial
    syncCartFromStorage();
    
    // Escuchar eventos personalizados
    window.addEventListener('cartUpdated', syncCartFromStorage);
    window.addEventListener('cartCleared', syncCartFromStorage);
    
    // Escuchar eventos de storage entre pestañas
    window.addEventListener('storage', function(event) {
        if (event.key === 'shoppingCart') {
            syncCartFromStorage();
        }
    });
});
// Función para añadir producto al carrito
function addToCart(button) {
    const productCard = button.closest('.product-list-card');
    const productId = productCard.getAttribute('data-product-id');
    const productName = productCard.querySelector('.title').textContent;
    const productPrice = parseFloat(productCard.getAttribute('data-price').trim());
    const quantity = parseInt(productCard.querySelector('.number').value);
    const nota = productCard.querySelector('.nota-producto').value;
    
    // Buscar si el producto ya está en el carrito
    const existingProduct = cart.find(item => item.id === productId && item.nota === nota);
    
    if (existingProduct) {
        // Si ya existe con la misma nota, actualizar la cantidad
        existingProduct.quantity += quantity;
    } else {
        // Si no existe, añadirlo al carrito
        cart.push({
            id: productId,
            name: productName,
            price: productPrice,
            quantity: quantity,
            image: productCard.querySelector('img').src,
            nota: nota // Agregamos la nota al objeto del producto
        });
    }
    
    // Actualizar la vista del carrito
    updateCartView();
    saveCartToStorage();
    // Mostrar feedback al usuario
    alert(`${productName} añadido al carrito (${quantity} unidades)`);
    
}

// Función para actualizar la vista del carrito
function updateCartView() {
    const cartItemsContainer = document.getElementById('cart-items');
    const subtotalElement = document.getElementById('cart-subtotal');
    
    // Limpiar el contenedor
    cartItemsContainer.innerHTML = '';
    
    if (cart.length === 0) {
        cartItemsContainer.innerHTML = '<li class="empty-cart-message">Tu carrito está vacío</li>';
        subtotalElement.textContent = '$0.00';
        return;
    }
    
    let subtotal = 0;
    
    // Añadir cada producto al carrito
    cart.forEach((product, index) => {
        const productTotal = product.price * product.quantity;
        subtotal += productTotal;
        
        const productItem = document.createElement('li');
        productItem.className = 'product-item mb-24';
        productItem.innerHTML = `
            <a href="">
                <span class="item-image">
                    <img src="${product.image}" alt="${product.name}">
                </span>
            </a>
            <div class="product-text">
                <a href="">
                    <h6 class="mb-16 dark-gray">${product.name}</h6>
                    ${product.nota ? '<span class="nota-especial" style="display: block; font-size: 12px; color: #666; margin-bottom: 8px;">Con nota especial</span>' : ''}
                </a>
                <div class="qp_row">
                    <div class="quantity quantity-wrap">
                        <div class="decrement" onclick="updateCartQuantity(${index}, -1)"><i class="fa-solid fa-dash"></i></div>
                        <input type="text" name="quantity" value="${product.quantity}" maxlength="2" size="1" class="number" id="cart-qty-${index}">
                        <div class="increment" onclick="updateCartQuantity(${index}, 1)"><i class="fa-solid fa-plus-large"></i></div>
                    </div>
                    <h5 class="dark-gray">$${(productTotal).toFixed(2)}</h5>
                    <button class="remove-item" onclick="removeFromCart(${index})"><i class="fa-solid fa-trash"></i></button>
                </div>
            </div>
        `;
        
        cartItemsContainer.appendChild(productItem);
        
        // Añadir línea divisoria si no es el último elemento
        if (index < cart.length - 1) {
            const divider = document.createElement('li');
            divider.className = 'vr-line mb-24';
            cartItemsContainer.appendChild(divider);
        }
    });
    
    // Actualizar subtotal
    subtotalElement.textContent = `$${subtotal.toFixed(2)}`;
}

// Función para actualizar la cantidad de un producto en el carrito
function updateCartQuantity(index, change) {
    const newQuantity = cart[index].quantity + change;
    if (newQuantity < 1) return;
    cart[index].quantity = newQuantity;
    updateCartView();
    saveCartToStorage();
}

// Función para eliminar un producto del carrito
function removeFromCart(index) {
    cart.splice(index, 1);
    updateCartView();
    saveCartToStorage();
}

