                </div>
                <p class="lead">$${productTotal.toFixed(2)}</p>
            `;
            orderSummary.appendChild(productRow);
        });

        const total = calculateTotal(subtotal, deliveryOption);
        orderTotalElement.textContent = `$${total.toFixed(2)}`;
    }

    // Configurar el estado inicial
